        super.onViewCreated(view, savedInstanceState)
        recyclerView = view.findViewById(R.id.qura_rv)
        adapter = SuraNameAdapter(list)
        adapter.callBack = object : SuraNameAdapter.CallBack {
            override fun onItemClicked(positoin: Int, suraName: String) {
                Toast.makeText(context, suraName, Toast.LENGTH_LONG).show()
                showSuraDetails(positoin, suraName)
            }

        }
        recyclerView.adapter = adapter
    }

    fun showSuraDetails(pos: Int, name: String) {
        val intent = Intent(context, SuraDetails::class.java)
        intent.putExtra(Constant.ESTRA_SURA_NAME, name)
        intent.putExtra(Constant.ESTRA_SURA_POSITION, pos)
        startActivity(intent)
    }

